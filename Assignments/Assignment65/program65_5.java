import java.io.*;
import java.util.*;

class program65_5
{
/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OnBit
//  Description :   Returns the number with its first 4 bits ON
//  Input :         int 
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          06/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static int OnBit(int iNo)
    {
        int iMask = 1;   
        int iResult = 0;

        iMask = 0x0000000f;

        iResult = iNo | iMask;

        return iResult;
    }

/////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        int iNo = 0;
        int iResult = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter number: ");
        iNo = sobj.nextInt();

        iResult = OnBit(iNo);

        System.out.println("Number converted to : " + iResult);
    }
}


