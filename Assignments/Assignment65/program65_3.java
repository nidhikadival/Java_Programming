import java.io.*;
import java.util.*;

class program65_3
{
/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ToggleBit
//  Description :   Returns the number with its 7th bit toggled
//  Input :         int 
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          06/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static int ToggleBit(int iNo)
    {
        int iMask = 1;   
        int iResult = 0;

        iMask = 0x00000040;

        iResult = iNo ^ iMask;

        return iResult;
    }

/////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        int iNo = 0;
        int iResult = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter number: ");
        iNo = sobj.nextInt();

        iResult = ToggleBit(iNo);

        System.out.println("Number converted to : " + iResult);
    }
}


