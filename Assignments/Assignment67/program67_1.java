import java.io.*;
import java.util.*;

class program67_1
{
/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountOne
//  Description   : Counts number of ON bits in the given number
//  Input         : int
//  Output        : int
//  Author        : Nidhi Girish Kadival
//  Date          : 06/02/2026
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static int CountOne(int iNo)
    {
        int iCnt = 0;

        while(iNo != 0)
        {
            iNo = iNo & (iNo - 1);
            iCnt++;
        }
        return iCnt;
    }

/////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function of the application
//
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        int iNo = 0;
        int iResult = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter number: ");
        iNo = sobj.nextInt();
        iResult = CountOne(iNo);

        System.out.println("Number of ON bits : " + iResult);
    }
}

