import java.io.*;
import java.util.*;

class program66_3
{

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        int iNo = 0;
        int iPos = 0;
        int iMask = 0x1;
        int iResult = 0;

        Scanner sobj = new Scanner(System.in);

        System.out.println("Enter number: ");
        iNo = sobj.nextInt();

        System.out.println("Enter position: ");
        iPos = sobj.nextInt();

        iMask = iMask << (iPos - 1);

        iResult = iNo | iMask;

        System.out.println("Updated number: " + iResult);
    }
}



